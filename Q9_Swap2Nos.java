package githubWeeks2Projects;

public class Q9_Swap2Nos {
	
	public static void main(String args[])
	{
		int no1 = 1 , no2 = 2, temp ;
		
		System.out.println("no1 = "+no1+" no2 = "+no2);
		
		System.out.println("swapping.....");
		
		temp = no1;
		no1 = no2;
		no2 = temp;
		
		System.out.println("no1 : "+no1+" no2 : "+no2);
	}
}
