package githubWeeks2Projects;

public class Q15_SwitchCase {

	public static void main(String args[])
	{
		int no = 2;
		
		switch(no) {
		case 1:
		{
			System.out.println("Number is : "+no);
			break;
		}
		case 2:
		{
			System.out.println("Number is : "+no);
			break;
		}
		case 3:
		{
			System.out.println("Number is : "+no);
			break;
		}
		default:
		{
			System.out.println("Number does not matches the cases !");
			break;
		}
		}
	}
}
